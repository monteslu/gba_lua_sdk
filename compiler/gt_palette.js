// redmean color distance (a cheap perceptual metric); returns the nearest
// palette BYTE (0-255) to the requested (r,g,b).
export function nearestColorByte(r, g, b) {
  r &= 255; g &= 255; b &= 255;
  let best = 0, bestD = Infinity;
  for (let i = 0; i < 256; i++) {
    const p = GT_CAPTURE_PALETTE[i];
    const rm = (r + p[0]) >> 1;
    const dr = r - p[0], dg = g - p[1], db = b - p[2];
    const d = (((512 + rm) * dr * dr) >> 8) + 4 * dg * dg + (((767 - rm) * db * db) >> 8);
    if (d < bestD) { bestD = d; best = i; }
  }
  return best;
}
